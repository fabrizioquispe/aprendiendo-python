"""
Una función es un bloque de código que solo se ejecuta cuando se le llama.

Puede pasar datos, conocidos como parámetros, a una función.

Una función puede devolver datos como resultado.
"""

#Definimos la creacion de una funcion
def my_function():
    print("Esta es una funcion perrillo")

#Llamamos a la funcion my_function creada 
my_function();

#Ejemplo de funcion matemático
A = 20
B = 30
def function_suma():
    print(A + B)
function_suma()

def function_resta():
    print(A - B)
function_resta()

def function_multiplicacion():
    print(A * B)
function_multiplicacion()

def function_division():
    print(A / B)
function_division()